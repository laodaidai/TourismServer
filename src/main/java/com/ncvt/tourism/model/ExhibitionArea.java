package com.ncvt.tourism.model;

public class ExhibitionArea {
    private Integer exhibitionAreaId;

    private String exhibitionAreaName;

    private String exhibitionAreaAddress;

    private String exhibitionAreaPic;

    public Integer getExhibitionAreaId() {
        return exhibitionAreaId;
    }

    public void setExhibitionAreaId(Integer exhibitionAreaId) {
        this.exhibitionAreaId = exhibitionAreaId;
    }

    public String getExhibitionAreaName() {
        return exhibitionAreaName;
    }

    public void setExhibitionAreaName(String exhibitionAreaName) {
        this.exhibitionAreaName = exhibitionAreaName;
    }

    public String getExhibitionAreaAddress() {
        return exhibitionAreaAddress;
    }

    public void setExhibitionAreaAddress(String exhibitionAreaAddress) {
        this.exhibitionAreaAddress = exhibitionAreaAddress;
    }

    public String getExhibitionAreaPic() {
        return exhibitionAreaPic;
    }

    public void setExhibitionAreaPic(String exhibitionAreaPic) {
        this.exhibitionAreaPic = exhibitionAreaPic;
    }
}