package com.ncvt.tourism.model;

public class ScenicPic {
    private Integer scenicPicId;

    private Integer scenicSpotId;

    private String scenicPicUrl;

    public Integer getScenicPicId() {
        return scenicPicId;
    }

    public void setScenicPicId(Integer scenicPicId) {
        this.scenicPicId = scenicPicId;
    }

    public Integer getScenicSpotId() {
        return scenicSpotId;
    }

    public void setScenicSpotId(Integer scenicSpotId) {
        this.scenicSpotId = scenicSpotId;
    }

    public String getScenicPicUrl() {
        return scenicPicUrl;
    }

    public void setScenicPicUrl(String scenicPicUrl) {
        this.scenicPicUrl = scenicPicUrl;
    }
}